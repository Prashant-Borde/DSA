let monthQuarter = parseInt(prompt("Enter Quarter of a month : "));

switch (monthQuarter){

    case 1 :
        alert("January, February, March");
    break;

    case 2 :
        alert("April, May, June ");
    break;

    case 3 :
        alert("July, August, September");
    break;

    case 4 :
        alert("Octomber, November, December");
    break;

    default :
        alert("Invalid Number !");
    
}
